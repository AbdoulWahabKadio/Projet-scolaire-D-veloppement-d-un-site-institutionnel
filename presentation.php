    <h3>Historique de l'INSEA</h3>
    <section class="historique">

        <p>
            Créé en 1961 sous l’appellation « Centre de formation des ingénieurs des travaux de la statistique »,
            la dénomination Institut National de Statistique et d’Economie Appliquée (INSEA) a été adoptée en 1967
            en application du Décret Royal n° 532-67. Ce Décret a été modifié et complété, notamment par le Décret
            n° 2-99-804 du 6 Chaoual 1420 (13 janvier 2000).
        </p>
        <p>
            - Jusqu'en 1974, L'INSEA se limitait à la formation des ingénieurs d'application de la statistique en
            trois ans et des adjoints techniques en deux ans mais depuis cette date, et compte tenu des besoins
            importants dans le domaine de l'informatique, l'Institut a introduit un cycle de formation des Ingénieurs
            Analystes
            et un cycle de Programmeurs.
        </p>

        <p>
            -En 1983, un cycle supérieur de formation d'Analyste Concepteur et un autre de Statisticien-Démographe
            ont été introduits.
        </p>

        <p>
            -En 1990, l'INSEA a mis fin à la formation des cadres moyens (Adjoints Technique et Programmeurs).
        </p>

        <p>
            -En 1995, l'INSEA a opéré un changement majeur dans son système d'enseignement en offrant une formation,
            sur trois ans, menant à un diplôme d'Ingénieur d'Etat de l'INSEA.
        </p>

        <p>
            -A partir de 2011, la formation à l'INSEA est organisée en 3 cycles :
            Cycle Ingénieur, Cycle du Master, et Cycle de Doctorat.
        </p>

